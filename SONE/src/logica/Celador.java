package logica;

public class Celador extends Persona {

	protected char Nombre;
	protected char Apellido;
	
	
	public char getNombre() {
		return Nombre;
	}
	public void setNombre(char nombre) {
		Nombre = nombre;
	}
	public char getApellido() {
		return Apellido;
	}
	public void setApellido(char apellido) {
		Apellido = apellido;
	}
	
	
	

}
